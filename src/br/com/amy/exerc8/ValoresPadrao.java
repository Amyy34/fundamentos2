package br.com.amy.exerc8;

public class ValoresPadrao {
    byte b;
    short s;
    int i;
    float f;
    double d;
    boolean bl;
    char c;

    public static void main(String[] args) {
        ValoresPadrao valores = new ValoresPadrao();
        System.out.println(valores.b);
        System.out.println(valores.s);
        System.out.println(valores.i);
        System.out.println(valores.f);
        System.out.println(valores.d);
        System.out.println(valores.bl);
        System.out.println(valores.c);
    }
}
